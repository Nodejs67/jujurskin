/**
 * Ambil bahan aktif yang punya kadar/persentase RESMI dari key_ingredients
 * (mis. "Niacinamide 10%", "Salicylic Acid 2%"). Dipakai untuk badge persen menonjol.
 * Hanya yang benar-benar mencantumkan angka — tidak menebak.
 */
export function getActivesWithPercent(product: Product): { name: string; percent: string }[] {
  const re = /(\d+(?:[.,]\d+)?\s*%)/;
  return product.key_ingredients
    .map((ing) => {
      const m = ing.match(re);
      if (!m) return null;
      const percent = m[1].replace(/\s+/g, "");
      const name = ing.replace(re, "").replace(/\(\s*\)/g, "").trim();
      return { name, percent };
    })
    .filter((x): x is { name: string; percent: string } => x !== null);
}

export function getProductsByCategory(category: ProductCategory): Product[] {
  return PRODUCTS.filter((p) => p.category === category);
}

export function getProductsBySkinType(skinType: string): Product[] {
  return PRODUCTS.filter((p) =>
    p.skin_types.includes(skinType as SkinTypeMatch) || p.skin_types.includes("semua tipe")
  );
}

export function getProductsByBudget(maxPrice: number): Product[] {
  return PRODUCTS.filter((p) => p.price_min <= maxPrice);
}

export function searchProducts(query: string): Product[] {
  const q = query.toLowerCase();
  return PRODUCTS.filter(
    (p) =>
      p.name.toLowerCase().includes(q) ||
      p.brand.toLowerCase().includes(q) ||
      p.key_ingredients.some((i) => i.toLowerCase().includes(q)) ||
      p.concerns.some((c) => c.toLowerCase().includes(q))
  );
}

